package capitulo02;

public class PrimitivosLiterais {
	public static void main(String[] args) {
		
		// TIPOS PRIMITIVOS NUMERICOS INTEIROS
		int a = 0b101010101111; // REPRESENTACAO BINARIA
		int b = 0100; // REPRESENTACAO OCTAL
		int c = 0x100; // REPRESENTACAO HEXADECIMAL
		int d = 10_000_000; // MELHORIA VIZUAL PARA O DESENVOLVEDOR 
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		
	}
}
