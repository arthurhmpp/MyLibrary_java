package capitulo02;

public class TiposPrimitivos2 {

	public static void main (String[] args) {
		// TIPOS PRIMITIVO DE CARACTERE
		char letra1 = 'Ç'; // REPRESENTACAO DO CARACTERE
		char letra2 = 199; // REPRESENTACAO DO CODIGO INTEIRO DO CARACTERE
		char letra3 = '\u00c7'; // REPRESENTACAO DO CODIGO UNICODE DO CARACTERE
		
		System.out.println("char letra1 = " + letra1);
		System.out.println("char letra2 = " + letra2);
		System.out.println("char letra3 = " + letra3);
		
		// TIPOS PRIMITIVO DE LOGICO
		boolean teste1 = true;
		boolean teste2 = false;
		
		System.out.println("boolean teste1 = " + teste1);
		System.out.println("boolean teste2 = " + teste2);
	}
}
