package capitulo03;

public class OperadoresIncrementaisDecrementais {
	public static void main(String[] args) {
		int i = 0;
		
		System.out.println(i);
		i++; // i = i + 1
		System.out.println(i);
		i++; // i = i + 1
		System.out.println(i); 
		++i; // i = i + 1
		System.out.println(i);
		System.out.println(++i);
		System.out.println(i++);
		System.out.println(i);
		
		// VALOR ATUALIZADO DE I = 5
		System.out.println(i--);
		System.out.println(--i);
		System.out.println(i);
		
	}
}
